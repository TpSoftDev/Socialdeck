/*-------------------------- message_enums.dart -----------------------------*/
// Shared enums for message components in the SocialDeck design system
// These enums define message types and their visual configurations
/*--------------------------------------------------------------------------*/

//------------------------------- Enums -------------------------------------//
enum SDeckMessageType {
  error, // Communicates validation errors, failures, or critical issues
  success, // Communicates successful operations, confirmations, achievements
  warning, // Communicates cautions, important notes, or things to be aware of
  info, // Communicates helpful information, tips, or neutral notifications
  note, // Communicates hints, neutral messages, or supplementary information
}
